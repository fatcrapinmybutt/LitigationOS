class CaseReporter:
    """Generates status reports from per-case databases."""

    def report(self, lanes: Optional[List[str]] = None):
        """Print a comprehensive status report."""
        target_lanes = lanes or list(LANES.keys())
        width = 72

        print(f"\n{'=' * width}")
        print("LITIGATION STATUS REPORT".center(width))
        print(f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}".center(width))
        print(f"{'=' * width}\n")

        for lane in target_lanes:
            self._report_lane(lane, width)

    def _report_lane(self, lane: str, width: int):
        """Report on a single lane."""
        cfg = LANES[lane]
        db_path = CASE_DB_DIR / cfg["db_file"]

        print(f"{'─' * width}")
        print(f" LANE {lane}: {cfg['case_name']}")
        print(f" Case No. {cfg['case_number']} | {cfg['judge']} | {cfg['court']}")
        print(f"{'─' * width}")

        if not db_path.exists():
            print("  ⚠  Database not built. Run: python case_database_builder.py build\n")
            return

        conn = sqlite3.connect(str(db_path))
        conn.row_factory = sqlite3.Row

        tables = [
            ("evidence", "Evidence Items"),
            ("claims", "Active Claims"),
            ("timeline", "Timeline Events"),
            ("witnesses", "Witnesses"),
            ("authorities", "Legal Authorities"),
            ("opposition_analysis", "Opposition Notes"),
            ("damages", "Damage Categories"),
            ("filings", "Filings"),
            ("action_items", "Action Items"),
        ]

        for table, label in tables:
            try:
                count = conn.execute(f"SELECT COUNT(*) FROM {table}").fetchone()[0]
                status = "✓" if count > 0 else "○"
                print(f"  {status} {label}: {count}")
            except Exception:
                print(f"  ✗ {label}: error")

        # Top claims by strength
        try:
            claims = conn.execute(
                "SELECT claim_type, strength_score FROM claims "
                "WHERE status = 'active' ORDER BY strength_score DESC LIMIT 5"
            ).fetchall()
            if claims:
                print(f"\n  Top Claims:")
                for c in claims:
                    bar = "█" * (c["strength_score"] // 10) if c["strength_score"] else ""
                    print(f"    {c['claim_type']:<30} [{bar:<10}] {c['strength_score'] or 0}/100")
        except Exception:
            pass

        # Urgent action items
        try:
            items = conn.execute(
                "SELECT description, priority FROM action_items "
                "WHERE status IN ('pending','in_progress') ORDER BY priority LIMIT 5"
            ).fetchall()
            if items:
                print(f"\n  Pending Actions:")
                for item in items:
                    pri = {1: "🔴", 2: "🟡", 3: "⚪", 4: "⚫"}.get(item["priority"], "⚪")
                    print(f"    {pri} {item['description'][:60]}")
        except Exception:
            pass

        # Filed documents
        try:
            filings = conn.execute(
                "SELECT title, status FROM filings ORDER BY created_at DESC LIMIT 5"
            ).fetchall()
            if filings:
                print(f"\n  Recent Filings:")
                for f in filings:
                    emoji = {"draft": "📝", "ready": "✅", "filed": "📬", "served": "📨"}.get(
                        f["status"], "📄"
                    )
                    print(f"    {emoji} [{f['status']}] {f['title'][:50]}")
        except Exception:
            pass

        conn.close()
        print()


