class CaseDatabaseBuilder:
    """Builds per-case SQLite databases from the central case_analysis.db."""

    def __init__(self):
        self.source_conn: Optional[sqlite3.Connection] = None
        self.ai = LocalAI() if LocalAI else None
        self._exhibit_counters: Dict[str, int] = {}

    def _open_source(self) -> sqlite3.Connection:
        """Open the source analysis database."""
        if not SOURCE_DB.exists():
            log.warning(f"Source DB not found: {SOURCE_DB}")
            log.info("Creating empty source DB structure for forward compatibility")
            conn = sqlite3.connect(str(SOURCE_DB))
            conn.row_factory = sqlite3.Row
            return conn
        conn = sqlite3.connect(str(SOURCE_DB))
        conn.row_factory = sqlite3.Row
        return conn

    def _open_case_db(self, lane: str) -> sqlite3.Connection:
        """Open (or create) a per-case database."""
        cfg = LANES[lane]
        db_path = CASE_DB_DIR / cfg["db_file"]
        CASE_DB_DIR.mkdir(parents=True, exist_ok=True)
        conn = sqlite3.connect(str(db_path))
        conn.row_factory = sqlite3.Row
        conn.executescript(CASE_DB_SCHEMA)
        conn.commit()
        log.info(f"Case DB ready: {db_path}")
        return conn

    def _next_exhibit(self, lane: str) -> str:
        """Generate the next exhibit number for a lane (e.g., A-001)."""
        prefix = LANES[lane]["exhibit_prefix"]
        count = self._exhibit_counters.get(lane, 0) + 1
        self._exhibit_counters[lane] = count
        return f"{prefix}-{count:03d}"

    def _determine_lane(self, row: sqlite3.Row, target_lane: str) -> bool:
        """Check whether a source row belongs to the target lane."""
        case_lane = row["case_lane"] if "case_lane" in row.keys() else None
        if not case_lane:
            return True  # If no lane assigned, include in all
        lane_val = str(case_lane).strip().upper()
        if lane_val == target_lane:
            return True
        cfg = LANES[target_lane]
        for key in cfg["lane_keys"]:
            if key.lower() in str(case_lane).lower():
                return True
        return False

    def _populate_case_info(self, conn: sqlite3.Connection, lane: str):
        """Insert core case metadata."""
        cfg = LANES[lane]
        info_pairs = [
            ("case_number", cfg["case_number"]),
            ("case_name", cfg["case_name"]),
            ("court", cfg["court"]),
            ("county", cfg["county"]),
            ("judge", cfg["judge"]),
            ("case_type", cfg["case_type"]),
            ("plaintiff", cfg["plaintiff"]),
            ("defendant", cfg["defendant"]),
            ("exhibit_prefix", cfg["exhibit_prefix"]),
            ("lane", lane),
            ("court_level", cfg["court_level"]),
            ("division", cfg["division"]),
            ("plaintiff_status", "Pro Se"),
            ("build_timestamp", datetime.now().isoformat()),
        ]
        for key, value in info_pairs:
            conn.execute(
                "INSERT OR REPLACE INTO case_info (key, value, updated_at) VALUES (?, ?, datetime('now'))",
                (key, value),
            )
        conn.commit()
        log.info(f"  Lane {lane}: case_info populated ({len(info_pairs)} entries)")

    def _populate_evidence(self, src: sqlite3.Connection, dst: sqlite3.Connection, lane: str):
        """Map factual_findings → evidence table with exhibit numbers."""
        rows = src.execute(
            "SELECT ff.*, af.path as source_path FROM factual_findings ff "
            "LEFT JOIN analyzed_files af ON ff.file_id = af.id"
        ).fetchall()

        count = 0
        for row in rows:
            if not self._determine_lane(row, lane):
                continue
            exhibit = self._next_exhibit(lane)
            dst.execute(
                "INSERT INTO evidence "
                "(exhibit_number, source_file_id, finding_id, description, evidence_type, "
                "finding_text, source_path, page_number, admissibility, relevance, weight, "
                "authentication, notes) "
                "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)",
                (
                    exhibit,
                    row["file_id"],
                    row["id"],
                    row["finding_text"][:200] if row["finding_text"] else "Finding",
                    row["finding_type"] or "documentary",
                    row["finding_text"],
                    row["source_path"] if "source_path" in row.keys() else None,
                    row["page_number"],
                    row["admissibility_score"] or 0,
                    row["relevance_score"] or 0,
                    row["weight_score"] or 0,
                    row["authentication_level"] or "pending",
                    f"Auto-imported from analysis. Context: {(row['context'] or '')[:100]}",
                ),
            )
            count += 1
        dst.commit()
        log.info(f"  Lane {lane}: {count} evidence items imported")

    def _populate_claims(self, src: sqlite3.Connection, dst: sqlite3.Connection, lane: str):
        """Map legal_issues → claims table."""
        rows = src.execute("SELECT * FROM legal_issues").fetchall()
        count = 0
        claim_num = 0
        for row in rows:
            if not self._determine_lane(row, lane):
                continue
            claim_num += 1

            # Gather supporting evidence from evidence_chain
            chain_rows = src.execute(
                "SELECT finding_id, strength, explanation FROM evidence_chain "
                "WHERE supports_issue_id = ?",
                (row["id"],),
            ).fetchall()
            supporting = json.dumps([
                {"finding_id": c["finding_id"], "strength": c["strength"]}
                for c in chain_rows
            ]) if chain_rows else "[]"

            dst.execute(
                "INSERT INTO claims "
                "(claim_number, issue_id, claim_type, description, legal_basis, "
                "strength_score, supporting_evidence, notes) "
                "VALUES (?, ?, ?, ?, ?, ?, ?, ?)",
                (
                    claim_num,
                    row["id"],
                    row["issue_type"] or "general",
                    row["description"] or row["supporting_text"] or "Legal issue",
                    row["supporting_text"],
                    row["strength_score"] or 0,
                    supporting,
                    f"Auto-imported. Source page: {row['page_number']}",
                ),
            )
            count += 1
        dst.commit()
        log.info(f"  Lane {lane}: {count} claims imported")

    def _populate_timeline(self, src: sqlite3.Connection, dst: sqlite3.Connection, lane: str):
        """Map factual_findings with date patterns → timeline entries."""
        date_re = re.compile(
            r'(\d{4}-\d{2}-\d{2})|'
            r'(\d{1,2}/\d{1,2}/\d{4})|'
            r'((?:January|February|March|April|May|June|July|August|September|'
            r'October|November|December)\s+\d{1,2},?\s+\d{4})',
            re.IGNORECASE,
        )
        rows = src.execute("SELECT * FROM factual_findings").fetchall()
        count = 0
        for row in rows:
            if not self._determine_lane(row, lane):
                continue
            text = row["finding_text"] or ""
            match = date_re.search(text)
            event_date = None
            if match:
                raw = match.group(0)
                event_date = self._normalize_date(raw)

            if event_date or (row["finding_type"] and "date" in str(row["finding_type"]).lower()):
                significance = "background"
                if (row["relevance_score"] or 0) >= 70:
                    significance = "critical"
                elif (row["relevance_score"] or 0) >= 40:
                    significance = "important"

                dst.execute(
                    "INSERT INTO timeline "
                    "(event_date, event_type, description, finding_id, significance, notes) "
                    "VALUES (?, ?, ?, ?, ?, ?)",
                    (
                        event_date,
                        row["finding_type"] or "event",
                        text[:500],
                        row["id"],
                        significance,
                        row["context"][:200] if row["context"] else None,
                    ),
                )
                count += 1
        dst.commit()
        log.info(f"  Lane {lane}: {count} timeline entries imported")

    def _populate_witnesses(self, src: sqlite3.Connection, dst: sqlite3.Connection, lane: str):
        """Map entities → witnesses table."""
        rows = src.execute("SELECT * FROM entities").fetchall()
        count = 0
        for row in rows:
            # Check lane association
            case_lanes = row["case_lanes"] or ""
            if lane not in case_lanes.upper() and case_lanes.strip():
                # Check lane keys
                matched = False
                for key in LANES[lane]["lane_keys"]:
                    if key.lower() in case_lanes.lower():
                        matched = True
                        break
                if not matched and case_lanes.strip():
                    continue

            dst.execute(
                "INSERT INTO witnesses "
                "(entity_id, name, role, entity_type, notes) "
                "VALUES (?, ?, ?, ?, ?)",
                (
                    row["id"],
                    row["entity_name"] or row["normalized_name"],
                    row["role"] or "unknown",
                    row["entity_type"] or "person",
                    f"Mentions: {row['mention_count']}. Lanes: {case_lanes}",
                ),
            )
            count += 1
        dst.commit()
        log.info(f"  Lane {lane}: {count} witnesses imported")

    def _populate_authorities(self, src: sqlite3.Connection, dst: sqlite3.Connection, lane: str):
        """Map citations → authorities table."""
        rows = src.execute("SELECT * FROM citations").fetchall()
        count = 0
        for row in rows:
            if not self._determine_lane(row, lane):
                continue
            dst.execute(
                "INSERT INTO authorities "
                "(citation_id, citation_text, citation_type, rule_number, "
                "full_text, notes, verified) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                (
                    row["id"],
                    row["citation_text"],
                    row["citation_type"],
                    row["rule_number"],
                    row["context"],
                    f"Page {row['page_number']}, Line {row['line_number']}",
                    row["verified"] if row["verified"] is not None else 0,
                ),
            )
            count += 1
        dst.commit()
        log.info(f"  Lane {lane}: {count} authorities imported")

    def _populate_summary_actions(self, src: sqlite3.Connection, dst: sqlite3.Connection, lane: str):
        """Generate action items from analysis_summary recommendations."""
        rows = src.execute(
            "SELECT * FROM analysis_summary WHERE case_lane = ?", (lane,)
        ).fetchall()
        for row in rows:
            actions_raw = row["recommended_actions"] or ""
            for i, action in enumerate(actions_raw.split(";"), 1):
                action = action.strip()
                if not action:
                    continue
                dst.execute(
                    "INSERT INTO action_items (priority, category, description) VALUES (?, ?, ?)",
                    (2, "research", action),
                )

            weakest = row["weakest_areas"] or ""
            for area in weakest.split(";"):
                area = area.strip()
                if not area:
                    continue
                dst.execute(
                    "INSERT INTO action_items (priority, category, description) VALUES (?, ?, ?)",
                    (1, "evidence", f"Address weakness: {area}"),
                )
        dst.commit()

    @staticmethod
    def _normalize_date(raw: str) -> Optional[str]:
        """Attempt to normalize a date string to ISO format."""
        from datetime import datetime as dt
        for fmt in ("%Y-%m-%d", "%m/%d/%Y", "%B %d, %Y", "%B %d %Y"):
            try:
                return dt.strptime(raw.strip().replace(",", ", ").replace("  ", " "), fmt).strftime("%Y-%m-%d")
            except ValueError:
                continue
        return raw

    def build_lane(self, lane: str):
        """Build the complete per-case database for one lane."""
        if lane not in LANES:
            log.error(f"Unknown lane: {lane}. Valid lanes: {list(LANES.keys())}")
            return
        log.info(f"═══ Building Lane {lane}: {LANES[lane]['case_name']} ═══")

        src = self._open_source()
        dst = self._open_case_db(lane)

        try:
            self._populate_case_info(dst, lane)
            self._populate_evidence(src, dst, lane)
            self._populate_claims(src, dst, lane)
            self._populate_timeline(src, dst, lane)
            self._populate_witnesses(src, dst, lane)
            self._populate_authorities(src, dst, lane)
            self._populate_summary_actions(src, dst, lane)
            log.info(f"═══ Lane {lane} build complete ═══\n")
        finally:
            src.close()
            dst.close()

    def build_all(self):
        """Build databases for all three lanes."""
        log.info("Building all case databases...")
        for lane in LANES:
            self.build_lane(lane)
        log.info("All case databases built successfully.")


