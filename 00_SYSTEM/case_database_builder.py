    def _open_case_db(self, lane: str) -> Optional[sqlite3.Connection]:
        """Open a per-case database for reading."""
        cfg = LANES[lane]
        db_path = CASE_DB_DIR / cfg["db_file"]
        if not db_path.exists():
            log.warning(f"Case DB not found for Lane {lane}: {db_path}")
            log.info("Run 'build' first, or proceeding with template defaults.")
            return None
        conn = sqlite3.connect(str(db_path))
        conn.row_factory = sqlite3.Row
        return conn

    def _get_case_info(self, conn: Optional[sqlite3.Connection], lane: str) -> Dict[str, str]:
        """Retrieve case_info key-value pairs, falling back to LANES config."""
        cfg = LANES[lane]
        info = {
            "case_number": cfg["case_number"],
            "case_name": cfg["case_name"],
            "court": cfg["court"],
            "county": cfg["county"],
            "judge": cfg["judge"],
            "plaintiff": cfg["plaintiff"],
            "defendant": cfg["defendant"],
            "division": cfg["division"],
            "state_header": cfg["state_header"],
            "court_header": cfg["court_header"],
        }
        if conn:
            try:
                rows = conn.execute("SELECT key, value FROM case_info").fetchall()
                for row in rows:
                    info[row["key"]] = row["value"]
            except Exception:
                pass
        return info

    def _get_evidence(self, conn: Optional[sqlite3.Connection], limit: int = 20) -> List[Dict]:
        """Retrieve top evidence from case DB."""
        if not conn:
            return []
        try:
            rows = conn.execute(
                "SELECT * FROM evidence ORDER BY relevance DESC, admissibility DESC LIMIT ?",
                (limit,),
            ).fetchall()
            return [dict(r) for r in rows]
        except Exception:
            return []

    def _get_claims(self, conn: Optional[sqlite3.Connection]) -> List[Dict]:
        """Retrieve claims from case DB."""
        if not conn:
            return []
        try:
            rows = conn.execute(
                "SELECT * FROM claims WHERE status = 'active' ORDER BY strength_score DESC"
            ).fetchall()
            return [dict(r) for r in rows]
        except Exception:
            return []

    def _get_authorities(self, conn: Optional[sqlite3.Connection], favorable: bool = True) -> List[Dict]:
        """Retrieve authorities from case DB."""
        if not conn:
            return []
        try:
            rows = conn.execute(
                "SELECT * FROM authorities WHERE favorable = ? ORDER BY citation_type",
                (1 if favorable else 0,),
            ).fetchall()
            return [dict(r) for r in rows]
        except Exception:
            return []

    def _get_witnesses(self, conn: Optional[sqlite3.Connection]) -> List[Dict]:
        """Retrieve witnesses from case DB."""
        if not conn:
            return []
        try:
            rows = conn.execute("SELECT * FROM witnesses ORDER BY role, name").fetchall()
            return [dict(r) for r in rows]
        except Exception:
            return []

    def _get_timeline(self, conn: Optional[sqlite3.Connection], limit: int = 30) -> List[Dict]:
        """Retrieve timeline from case DB."""
        if not conn:
            return []
        try:
            rows = conn.execute(
                "SELECT * FROM timeline ORDER BY event_date ASC LIMIT ?", (limit,)
            ).fetchall()
            return [dict(r) for r in rows]
        except Exception:
            return []

